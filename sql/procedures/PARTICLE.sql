DROP PROCEDURE IF EXISTS PARTICLE;
DELIMITER |
CREATE PROCEDURE PARTICLE (IN VDATE_PUBLICATION DATE, IN VTAUX_POSITIVITE FLOAT, IN VTAUX_NEGATIVITE FLOAT,
    IN VTAUX_JOIE FLOAT, IN VTAUX_PEUR FLOAT, IN VTAUX_TRISTESSE FLOAT, IN VTAUX_COLERE FLOAT,
    IN VTAUX_SURPRISE FLOAT, IN VTAUX_DEGOUT FLOAT, IN VID_JOURNAL INT, OUT VID_ARTICLE INT)

BEGIN

    INSERT INTO ARTICLE (id_article, date_publication, taux_positivite, taux_negativite, taux_joie, taux_peur, taux_tristesse, taux_colere, taux_surprise, taux_degout, id_journal) VALUES (NULL,VDATE_PUBLICATION,VTAUX_POSITIVITE,VTAUX_NEGATIVITE,VTAUX_JOIE,VTAUX_PEUR,
     VTAUX_TRISTESSE,VTAUX_COLERE,VTAUX_SURPRISE,VTAUX_DEGOUT,VID_JOURNAL);
    SELECT LAST_INSERT_ID() INTO VID_ARTICLE;

END|         

DELIMITER ;
